<template>
    <div class="row">
        <div class="col text-truncate">
            Facebook:
            <a
                :href="currentAddress"
                target="_blank"
                rel="nofollow"
                v-b-tooltip.hover :title="currentAddress"
            >
                {{ currentAddress }}
            </a>
        </div>
        <div class="col-auto">
            <div
                class="fb-share-button"
                :data-href="currentAddress"
                data-layout="button_count"
                data-size="small"
                data-mobile-iframe="true"
            >
                <a
                    target="_blank"
                    :href="facebookShareButtonUrl"
                    class="fb-xfbml-parse-ignore"
                ></a>
            </div>
        </div>
        <div class="col-auto social-help">
            <guide>
                <template slot="header">
                    Facebook
                </template>
                <template slot="body">
                    Link to token creatorâ€™s Facebook.
                    Before adding it, we confirmed ownership.
                </template>
            </guide>
        </div>
    </div>
</template>

<script>
import {FiltersMixin} from '../../../mixins';
import Guide from '../../Guide';

export default {
    name: 'TokenFacebookAddressView',
    props: {
        address: String,
    },
    components: {
        Guide,
    },
    mixins: [FiltersMixin],
    data() {
        return {
            currentAddress: this.address,
        };
    },
    computed: {
        facebookShareButtonUrl: function() {
            return 'https://www.facebook.com/sharer/sharer.php?u='
                + encodeURIComponent(this.currentAddress)
                + '&amp;src=sdkpreparse';
        },
    },
};
</script>


