<template>
    <div class="post">
        <p v-if="post.content">
            <bbcode-view :value="post.content" class="post-content"/>
        </p>
        <p v-else>
            To see this post you need to have {{post.amount | toMoney | formatMoney}} {{post.token.name}} in your balance. Visit trade page and create buy order to get required tokens.
        </p>
        <span>
            {{ date }}
        </span>
        <a :href="$routing.generate('profile-view', {pageUrl: post.author.page_url})">
            {{ author }}
        </a>
        <button v-if="showEdit"
            class="btn btn-link p-0 delete-icon float-right text-decoration-none text-reset"
            :disabled="deleteDisabled"
            @click="deletePost"
        >
            <font-awesome-icon
                class="icon-edit c-pointer align-middle"
                icon="trash"
                transform="shrink-4 up-1.5"
            />
        </button>
        <button v-if="showEdit"
            class="btn btn-link p-0 edit-icon float-right text-decoration-none text-reset"
            :disabled="editDisabled"
            :href="$routing.generate('edit_post_page', {id: post.id})"
        >
            <font-awesome-icon
                class="icon-edit c-pointer align-middle"
                icon="edit"
                transform="shrink-4 up-1.5"
            />
        </button>
    </div>
</template>

<script>
import BbcodeView from '../bbcode/BbcodeView';
import moment from 'moment';
import {library} from '@fortawesome/fontawesome-svg-core';
import {faEdit, faTrash} from '@fortawesome/free-solid-svg-icons';
import {FontAwesomeIcon} from '@fortawesome/vue-fontawesome';
import {MoneyFilterMixin, NotificationMixin} from '../../mixins';

library.add(faEdit);
library.add(faTrash);

export default {
    name: 'Post',
    mixins: [
        MoneyFilterMixin,
        NotificationMixin,
    ],
    components: {
        BbcodeView,
        FontAwesomeIcon,
    },
    props: {
        post: Object,
        index: {
            type: Number,
            default: null,
        },
        showEdit: {
            type: Boolean,
            default: false,
        },
    },
    data() {
        return {
            deleteDisabled: false,
            editDisabled: false,
        };
    },
    computed: {
        author() {
            return `${this.post.author.firstName} ${this.post.author.lastName}`;
        },
        date() {
            return moment(this.post.createdAt).format('H:mm, MMM D, YYYY');
        },
    },
    methods: {
        deletePost() {
            this.deleteDisabled = true;
            this.$axios.single.post(this.$routing.generate('delete_post', {id: this.post.id}))
            .then((res) => {
               this.$emit('delete-post', this.index);
               this.notifySuccess(res.data.message);
               this.deleteDisabled = false;
            })
            .catch(() => {
                this.notifyError('Error deleting post.');
            });
        },
    },
};
</script>
